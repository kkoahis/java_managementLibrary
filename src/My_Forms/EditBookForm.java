    private void jButton_SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_SearchActionPerformed
        //check if the id fields or the isbn are empty
        if (jTextField_ID.getText().equals("") && jTextField_ISBN.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "You Need To Enter The ID Or the ISBN To Complete The Search ", "Empty ID or ISBN", 2);
        } else {
            try {
                //search book by id or isbn:
                My_Classes.Book selectedbook;

                int id = 0;
                id = Integer.parseInt(jTextField_ID.getText());
                String isbn = jTextField_ISBN.getText();
                selectedbook = book.searchBook(id, isbn);

                if (selectedbook != null) {
                    jTextField_ID.setText(String.valueOf(selectedbook.getBook_id()));
                    jTextField_ISBN.setText(selectedbook.getIsbn());
                    jTextField_Name.setText(selectedbook.getName());
                    jTextField_Publisher.setText(selectedbook.getPublisher());
                    jSpinner_Quanlity.setValue(selectedbook.getQuanlity());
                    jTextField_Price.setText(Double.toString(selectedbook.getPrice()));
                    jTextField_Book_Description.setText(selectedbook.getDescription());
                    jLabel_ID_Author.setText(String.valueOf(selectedbook.getAuthor_id()));
                    jLabel_ID_Genre.setText(String.valueOf(selectedbook.getGenre_id()));

                    //display the author full name
                    String fullname = (author.getAuthorByID(selectedbook.getAuthor_id())).getFirstName()
                            + " " + (author.getAuthorByID(selectedbook.getAuthor_id())).getLastName();
                    jTextField_Author.setText(fullname);

                    //display the selected book genre in the jcombobox
                    for (Map.Entry<String, Integer> entry : genHashMap.entrySet()) {
                        if (Objects.equals(selectedbook.getGenre_id(), entry.getValue())) {
                            jComboBox_Genre.setSelectedItem(entry.getKey());
                        }
                    }

                    //display the date
                    Date date_recieved = new SimpleDateFormat("yyyy-MM-dd").parse(selectedbook.getDate_received());
                    jDateChooser_DateRecived.setDate(date_recieved);

                    //display image:
                    byte[] image = selectedbook.getCover();
                    //we will display the image using the imagebyte
                    //so we will made the image path null
                    func.displayImage(238, 177, image, "", jLabel_Image1);
                } else {
                    JOptionPane.showMessageDialog(null, "No Books With This ID Found", "invalid id book", 2);
                }
            } catch (SQLException | NumberFormatException ex) {
                JOptionPane.showMessageDialog(null, "This Book Does Not Exist " + ex.getMessage(), "Book not found", 2);
            } catch (ParseException ex) {
                Logger.getLogger(EditBookForm.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_jButton_SearchActionPerformed

