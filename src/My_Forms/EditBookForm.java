    private void jButton_Edit_BookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_Edit_BookActionPerformed

        //edit a  Book
        //we will not eidt the isbn

        if (!verif()) {
            JOptionPane.showMessageDialog(null, "One Or More Fields Is Empty", "empty data", 2);
        } else {
            try {
                int id = Integer.parseInt(jTextField_ID.getText());
                String name = jTextField_Name.getText();
                String publisher = jTextField_Publisher.getText();
                String desciption = jTextField_Book_Description.getText();

                Integer author_id = Integer.parseInt(jLabel_ID_Author.getText());   //get the id author
                Integer genre_id = Integer.parseInt(jLabel_ID_Genre.getText());     //get the id genre
                Integer quanlity = Integer.parseInt(jSpinner_Quanlity.getValue().toString());

                double price = Double.parseDouble(jTextField_Price.getText());

                SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
                String received_Date = dateFormat.format(jDateChooser_DateRecived.getDate());
                Path path = Paths.get(imagePath);

                try {
                    //if the user want to change the image
                    byte[] img = Files.readAllBytes(path);
                    book.editBook(id, name, author_id, genre_id, quanlity, publisher, price, received_Date, desciption, img);
                } catch (IOException e) {   //if not want to change
                    book.editBook(id, name, author_id, genre_id, quanlity, publisher, price, received_Date, desciption, null);
                }
            } catch (SQLException ex) {
                Logger.getLogger(EditBookForm.class.getName()).log(Level.SEVERE, null, ex);
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(null, "You Enterred Wrong Data In A Number Field", "Wrong data number", 2);
            } catch (NullPointerException ex) {
                JOptionPane.showMessageDialog(null, "You Need To Select A Date", "Select date", 2);
            }
        }

    }//GEN-LAST:event_jButton_Edit_BookActionPerformed

