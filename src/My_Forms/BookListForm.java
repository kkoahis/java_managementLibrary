    private void jTable_Books_MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable_Books_MouseClicked
        //display the book on the table to the textfield
        //get book ID
        My_Classes.Book selectedbook = null;
        try {
            //search book by ID and print infor 
            Integer rowIndex = jTable_Books_.getSelectedRow();    //get the selected row index
            //get the book id from the jtable(the id is the first column [0] )
            Integer id = Integer.parseInt(jTable_Books_.getModel().getValueAt(rowIndex, 0).toString());

            selectedbook = book.getBookById(id);

            if (selectedbook != null) {
                jLabel_ISBN.setText(selectedbook.getIsbn());
                jLabel_Name.setText(selectedbook.getName());

                //we need to display the author name
                jLabel_Author.setText(author.getAuthorByID(selectedbook.getAuthor_id()).getFirstName()
                        + " " + author.getAuthorByID(selectedbook.getAuthor_id()).getLastName());

                //we need to display the Genres name
                jLabel_Genre.setText(genre.getGenresID(selectedbook.getGenre_id()).getName());
                jLabel_Publisher.setText(selectedbook.getPublisher());
                jLabel_Price.setText(Double.toString(selectedbook.getPrice()));

                //display image:
                byte[] image = selectedbook.getCover();
                //we will display the image using the imagebyte
                //so we will made the image path null
                func.displayImage(237, 170, image, "", jLabel_Image1);
            } else {
                JOptionPane.showMessageDialog(null, "No Book With This ID Found", "invalid id book", 2);
            }

        } catch (NumberFormatException ex) {
            //Logger.getLogger(EditMemberForm.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Enter a valid Member ID", "invalid id member", 3);
        } catch (SQLException ex) {
            Logger.getLogger(BookListForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jTable_Books_MouseClicked

