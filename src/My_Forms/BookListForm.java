    public void populateJtableWithBook(String query) {

        ArrayList<My_Classes.Book> bookList = book.booksLists(query);

        //jtable collums
        String[] colNames = {"ID", "ISBN", "TITLE", "AUTHOR", "GENRE", "QUANLITY", "PUBLISHER", "PRICE", "DATE_Re"};
        //jtable row
        Object[][] rows = new Object[bookList.size()][colNames.length];

        for (int i = 0; i < bookList.size(); i++) {
            rows[i][0] = bookList.get(i).getBook_id();
            rows[i][1] = bookList.get(i).getIsbn();
            rows[i][2] = bookList.get(i).getName();
            rows[i][3] = bookList.get(i).getAuthor_id();
            rows[i][4] = bookList.get(i).getGenre_id();
            rows[i][5] = bookList.get(i).getQuanlity();
            rows[i][6] = bookList.get(i).getPublisher();
            rows[i][7] = bookList.get(i).getPrice();
            rows[i][8] = bookList.get(i).getDate_received();
        }

        DefaultTableModel model = new DefaultTableModel(rows, colNames);
        jTable_Books_.setModel(model);
    }


